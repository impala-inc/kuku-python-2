# 「a」「b」「c」「d」がそれぞれどんな値となるかを予想してください

a = str('田中') + str('太郎')
b = int(200) + int(100)
c = list(['桃太郎', 'いぬ']) + list(['かに', 'くり'])
d = dict(blue='青', yellow='黄', red='赤')

print(a)  # => 田中太郎
print(b)  # => 300
print(c)  # => ['桃太郎', 'いぬ', 'かに', 'くり']
print(d)  # => {'blue': '青', 'yellow': '黄', 'red': '赤'}
